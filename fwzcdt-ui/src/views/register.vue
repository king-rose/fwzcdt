<template>
	<div class="register">
		<el-form ref="registerForm" :model="registerForm" :rules="registerRules" class="register-form">
			<h3 class="title">服务资产地图</h3>
			<el-form-item prop="userName">
				<el-input v-model="registerForm.userName" type="text" auto-complete="off" placeholder="账号">
					<svg-icon slot="prefix" icon-class="user" class="el-input__icon input-icon" />
				</el-input>
			</el-form-item>

			<el-form-item prop="email">
				<el-input v-model="registerForm.email" auto-complete="off" placeholder="邮箱">
					<svg-icon slot="prefix" icon-class="email" class="el-input__icon input-icon" />
				</el-input>
			</el-form-item>

			<el-form-item prop="nickName">
				<el-input v-model="registerForm.nickName" auto-complete="off" placeholder="昵称">
					<svg-icon slot="prefix" icon-class="nick" class="el-input__icon input-icon" />
				</el-input>
			</el-form-item>

			<el-form-item prop="password">
				<el-input v-model="registerForm.password" type="password" auto-complete="new-password" placeholder="密码">
					<svg-icon slot="prefix" icon-class="password" class="el-input__icon input-icon" />
				</el-input>
			</el-form-item>

			<el-form-item prop="confirmPassword">
				<el-input v-model="registerForm.confirmPassword" type="password" auto-complete="off" placeholder="确认密码">
					<svg-icon slot="prefix" icon-class="password" class="el-input__icon input-icon" />
				</el-input>
			</el-form-item>

			<el-form-item style="width:100%;">
				<el-button
					:loading="loading"
					size="medium"
					type="primary"
					style="width:100%;"
					@click.native.prevent="handleRegister"
				>
					<span v-if="!loading">注 册</span>
					<span v-else>注 册 中...</span>
				</el-button>
				<div style="float: right;">
					<router-link class="link-type" :to="'/login'">使用已有账户登录</router-link>
				</div>
			</el-form-item>
		</el-form>
		<!--  底部  -->
		<div class="el-register-footer">
			<span> 赞同科技 </span>
		</div>
	</div>
</template>

<script>
import { register } from "@/api/login";

export default {
	name: "Register",
	data() {
		const equalToPassword = (rule, value, callback) => {
			if (this.registerForm.password !== value) {
				callback(new Error("两次输入的密码不一致"));
			} else {
				callback();
			}
		};
		const checkEmail = (rule, value, callback) => {
			//验证邮箱的正则表达式
			const regEmail = /^([a-zA-Z0-9_-])+@([a-zA-Z0-9_-])+(\.[a-zA-Z0-9_-])+/;
			if(!regEmail.test(value)){
				//合法的邮箱
				callback(new Error("请输入合法的邮箱！"));
			}else
				callback();
		};
		return {
			codeUrl: "",
			registerForm: {
				userName: "",
				nickName:"",
				password: "",
				confirmPassword: "",
				code: "",
				uuid: ""
			},
			registerRules: {
				userName: [
					{ required: true, trigger: "blur", message: "请输入您的账号" },
					{ min: 2, max: 20, message: '用户账号长度必须介于 2 和 20 之间', trigger: 'blur' }
				],
				nickName:[
					{ required: true, trigger: "blur", message: "请输入您的昵称" },
				],
				email:[
					{ required: true, trigger: "blur", message: "请输入您的邮箱" },
					{ required: true, validator: checkEmail, trigger: "blur" }

				],
				password: [
					{ required: true, trigger: "blur", message: "请输入您的密码" },
					{ min: 5, max: 20, message: '用户密码长度必须介于 5 和 20 之间', trigger: 'blur' }
				],
				confirmPassword: [
					{ required: true, trigger: "blur", message: "请再次输入您的密码" },
					{ required: true, validator: equalToPassword, trigger: "blur" }
				],
			},
			loading: false,
		};
	},
	created() {

	},
	methods: {
		handleRegister() {
			this.$refs.registerForm.validate(valid => {
				if (valid) {
					this.loading = true;
					register(this.registerForm).then(res => {
						const userName = this.registerForm.userName;
						this.$alert("<font color='red'>恭喜你，您的账号 " + userName + " 注册成功！</font>", '系统提示', {
							dangerouslyUseHTMLString: true,
							type: 'success'
						}).then(() => {
							this.$router.push("/login");
						}).catch(() => {});
					}).catch(() => {
						this.loading = false;
					})
				}
			});
		}
	}
};
</script>

